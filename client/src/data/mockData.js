// SHAP-like feature importance
export const getFeatureImportance = (patient) => {
  const latest = patient.timepoints[patient.timepoints.length - 1];
  const prev = patient.timepoints.length > 1 ? patient.timepoints[patient.timepoints.length - 2] : null;

  const tfDelta = prev ? latest.tumorFraction - prev.tumorFraction : 0;
  const vafMax = Math.max(...latest.mutations.map((m) => m.vaf));
  const newMuts = prev ? latest.mutations.length - prev.mutations.length : 0;

  const items = [
    {
      feature: 'Tumor Fraction Change (ΔTF)',
      importance: Math.abs(tfDelta) * 200,
      direction: tfDelta > 0 ? 'positive' : 'negative',
      value: `${tfDelta > 0 ? '+' : ''}${(tfDelta * 100).toFixed(1)}%`,
    },
    {
      feature: 'Max VAF (latest)',
      importance: vafMax * 100,
      direction: vafMax > 0.05 ? 'positive' : 'negative',
      value: `${(vafMax * 100).toFixed(1)}%`,
    },
    {
      feature: 'Methylation Score',
      importance: latest.methylationScore * 30,
      direction: latest.methylationScore > 0.5 ? 'positive' : 'negative',
      value: latest.methylationScore.toFixed(2),
    },
    {
      feature: 'Genome Instability',
      importance: latest.genomeInstabilityScore * 0.3,
      direction: latest.genomeInstabilityScore > 40 ? 'positive' : 'negative',
      value: `${latest.genomeInstabilityScore}/100`,
    },
    {
      feature: 'New Mutations Detected',
      importance: newMuts * 8,
      direction: newMuts > 0 ? 'positive' : 'negative',
      value: `${newMuts > 0 ? '+' : ''}${newMuts}`,
    },
    {
      feature: 'cfDNA Concentration',
      importance: (latest.totalCfDNA / 60) * 20,
      direction: latest.totalCfDNA > 30 ? 'positive' : 'negative',
      value: `${latest.totalCfDNA.toFixed(1)} ng/mL`,
    },
    {
      feature: 'Fragment Size Ratio',
      importance: latest.shortFragmentRatio * 25,
      direction: latest.shortFragmentRatio > 0.3 ? 'positive' : 'negative',
      value: latest.shortFragmentRatio.toFixed(2),
    },
    {
      feature: 'Resistance Mutation',
      importance: latest.mutations.some((m) => m.variant === 'T790M' || m.variant === 'Loss') ? 20 : 2,
      direction: latest.mutations.some((m) => m.variant === 'T790M' || m.variant === 'Loss')
        ? 'positive'
        : 'negative',
      value: latest.mutations.some((m) => m.variant === 'T790M' || m.variant === 'Loss')
        ? 'Detected'
        : 'None',
    },
  ];

  return items.sort((a, b) => b.importance - a.importance);
};